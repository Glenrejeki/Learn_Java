public class ForLoop {
  public static void main(String[] args) {
    
    /**.
     * Foor Loop -> Mengulang sekelompok pernyataan tertentu berulang kali selama 
     * kondisi terpenuhi
     * 
     * Iterasi -> mengulangi blok kode berdasarkan kondisi yang telah dibuat
     * for(insialisasi; kondisi; iterasi){
     * Blok kode yang diulang
     * }
     * Insialisasi -> Nilai awal untuk variabel pengontrol loop
     * Kondisi -> Kondisi yang harus dipenuhi agar loop terjadi
     */

     for(int i = 1; i <= 5; i++){
      System.out.println("Angka "+i);
     }


    
  }
}
