public class OperasiPerbandingan {
  public static void main(String[] args) {
    int x = 20;
    int y = 10;


    // Lebih besar dari (>)
    System.out.println(x+">"+y+": " +(x>y));

    // Lebih kecil dari (<)
    System.out.println(x+">"+y+": " +(x<y));

    // Lebih besar atau sama dengan  (>=)
    System.out.println(x+">"+y+": " +(x>y));

    // Lebih kecil atau sama dengan (<=)
    System.out.println(x+">"+y+": " +(x<=y));

    // Sama dengan  (==)
    System.out.println(x+">"+y+": " +(x==y));

    // Tidak sama dengan (!=)
    System.out.println(x+">"+y+": " +(x!=y));

  }
}
