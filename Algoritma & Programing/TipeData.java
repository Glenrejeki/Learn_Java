public class TipeData {

  public static void main(String[] args) {
    // String
    String name = "Glen Rejeki";
    System.out.println(name);

    // Char
    char huruf = 'b';
    System.out.println(huruf);

    // Boolean
    boolean dataBoolean = true;
    System.out.println(dataBoolean);

    // Number
    byte dataByte = 123;
    short dataShort = 12345;
    int dataInt = 1234567891;
    long dataLong = 123456789101112L;
    
    float dataFloat = 12.45F;
    double dataDouble = 12.234;

    System.out.println(dataByte);
    System.out.println(dataShort);
    System.out.println(dataInt);
    System.out.println(dataLong);
    System.out.println(dataFloat);
    System.out.println(dataDouble);
  }
}