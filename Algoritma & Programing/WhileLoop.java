public class WhileLoop {
  public static void main(String[] args) {
    /**.
     * While Loop -> Mengulang pernyataan selama kondisi tertentu terpenuhi
     */

     int a = 1; // insialisasi
     while (a<=10) { // kondisi
      System.out.println("Looping -"+a);
      a++; // iterasi 
     }
  }
}
